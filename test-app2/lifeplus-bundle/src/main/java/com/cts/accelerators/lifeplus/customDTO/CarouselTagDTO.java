package com.cts.accelerators.lifeplus.customDTO;

public class CarouselTagDTO {

	String imageReference;
	String title;
	String description;
	String navTitle;
	String navDescription;
	String buttonText;
	String link;
	
	
	/**
	 * @return the imageReference
	 */
	public String getImageReference() {
		return imageReference;
	}
	/**
	 * @param imageReference the imageReference to set
	 */
	public void setImageReference(String imageReference) {
		this.imageReference = imageReference;
	}
	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}
	/**
	 * @param title the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the navTitle
	 */
	public String getNavTitle() {
		return navTitle;
	}
	/**
	 * @param navTitle the navTitle to set
	 */
	public void setNavTitle(String navTitle) {
		this.navTitle = navTitle;
	}
	/**
	 * @return the navDescription
	 */
	public String getNavDescription() {
		return navDescription;
	}
	/**
	 * @param navDescription the navDescription to set
	 */
	public void setNavDescription(String navDescription) {
		this.navDescription = navDescription;
	}
	/**
	 * @return the buttonText
	 */
	public String getButtonText() {
		return buttonText;
	}
	/**
	 * @param buttonText the buttonText to set
	 */
	public void setButtonText(String buttonText) {
		this.buttonText = buttonText;
	}
	/**
	 * @return the link
	 */
	public String getLink() {
		return link;
	}
	/**
	 * @param link the link to set
	 */
	public void setLink(String link) {
		this.link = link;
	}
	
}
